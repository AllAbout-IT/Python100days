# 22. Mathematical Operations in Python
#
#   Mathematical        Programming
#   3 + 5               3 + 5
#   3 - 5               3 - 5
#   3 x 5               3 * 5
#   6 ÷ 3               6 / 3

print(6/3)
# If you use '/', It's written in float.
print(type(6/3))

# In addition
#   Mathematical        Programming
#   2²                  2 ** 2
print(2**3)
print(2**4)
#   
# PEMDAS(priority)
# 0.Parentheses     ->  ()
# 1.Exponents       ->  **
# 2.Multiplication  ->  *
# 3.Division        ->  /
# 4.Addition        ->  +
# 5.Subtraction     ->  -

print(3 * 3 + 3 / 3 - 3)
